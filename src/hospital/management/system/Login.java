    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        String name, pwd;
        name = uname.getText();
        pwd = password.getText();

        if (uname.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please insert your Username!");

        } else if (password.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please insert your Password!");
        }

        try {
            Registry reg = LocateRegistry.getRegistry(server_address, port);
            UserInterface user = (UserInterface) reg.lookup("UserService");

            String serverResponse = user.login(name, pwd);

            switch (serverResponse) {
                case "RECEPTIONIST":
                    Receptionist r = new Receptionist();
                    r.setVisible(true);
                    this.dispose();
                    break;
                case "ENT":
                    ListOfENTPatients ent = new ListOfENTPatients();
                    ent.setVisible(true);
                    this.dispose();
                    break;
                case "GENERAL":
                    myPatients p = new myPatients();
                    p.setVisible(true);
                    this.dispose();
                    break;
                case "false":
                    JOptionPane.showMessageDialog(null, "Invalid Login!");
                    break;
            }

        } catch (IOException e) {
            System.out.println(e.toString());

        } catch (JSONException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_loginbtnActionPerformed

